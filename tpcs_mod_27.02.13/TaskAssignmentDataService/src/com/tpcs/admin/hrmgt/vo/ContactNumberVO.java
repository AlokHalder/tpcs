package com.tpcs.admin.hrmgt.vo;

/**
 *
 * @author ALOK
 */
public class ContactNumberVO {
    private String name;
    private String category;
    private String contactNumber;
    private String alternateNumber;
    private String emailId;
    private String alternateEmailId;
    private String nickName;
    

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the category
     */
    public String getCategory() {
        return category;
    }

    /**
     * @param category the category to set
     */
    public void setCategory(String category) {
        this.category = category;
    }

    /**
     * @return the contactNumber
     */
    public String getContactNumber() {
        return contactNumber;
    }

    /**
     * @param contactNumber the contactNumber to set
     */
    public void setContactNumber(String contactNumber) {
        this.contactNumber = contactNumber;
    }

    /**
     * @return the emailId
     */
    public String getEmailId() {
        return emailId;
    }

    /**
     * @param emailId the emailId to set
     */
    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }

    /**
     * @return the nickName
     */
    public String getNickName() {
        return nickName;
    }

    /**
     * @param nickName the nickName to set
     */
    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    /**
     * @return the alternateNumber
     */
    public String getAlternateNumber() {
        return alternateNumber;
    }

    /**
     * @param alternateNumber the alternateNumber to set
     */
    public void setAlternateNumber(String alternateNumber) {
        this.alternateNumber = alternateNumber;
    }

    /**
     * @return the alternateEmailId
     */
    public String getAlternateEmailId() {
        return alternateEmailId;
    }

    /**
     * @param alternateEmailId the alternateEmailId to set
     */
    public void setAlternateEmailId(String alternateEmailId) {
        this.alternateEmailId = alternateEmailId;
    }
    
    
}
