/**
 *
 * @author TPCS
 */
public class ContactVO {
    
    private String  newname;
    private String newemail;
    private String newcompany;
    private String newsubject;
    private String newmsg;

    /**
     * @return the newname
     */
    public String getNewname() {
        return newname;
    }

    /**
     * @param newname the newname to set
     */
    public void setNewname(String newname) {
        this.newname = newname;
    }

    /**
     * @return the newemail
     */
    public String getNewemail() {
        return newemail;
    }

    /**
     * @param newemail the newemail to set
     */
    public void setNewemail(String newemail) {
        this.newemail = newemail;
    }

    /**
     * @return the newcompany
     */
    public String getNewcompany() {
        return newcompany;
    }

    /**
     * @param newcompany the newcompany to set
     */
    public void setNewcompany(String newcompany) {
        this.newcompany = newcompany;
    }

    /**
     * @return the newsubject
     */
    public String getNewsubject() {
        return newsubject;
    }

    /**
     * @param newsubject the newsubject to set
     */
    public void setNewsubject(String newsubject) {
        this.newsubject = newsubject;
    }

    /**
     * @return the newmsg
     */
    public String getNewmsg() {
        return newmsg;
    }

    /**
     * @param newmsg the newmsg to set
     */
    public void setNewmsg(String newmsg) {
        this.newmsg = newmsg;
    }
    
}
